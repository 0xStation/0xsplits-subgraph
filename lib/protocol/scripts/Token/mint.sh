tokenOwnerModule=0x74090bfb9b2F83a8B692756406c7bc585dA7043e #$(jq -r '.OwnerMint' out/addresses.json)
token=0xABf03EDC17De11e80008C3e89919b82AbA34521A

mintTokens() {
    minted=$(ETH_FROM=$ETH_FROM seth send $tokenOwnerModule \
        'mintTokens(address,address,uint256)' $token $1 $2 \
        --gas 6000000 --rpc-url $ETH_RPC_URL
    )
    echo "Tokens minted to $1: $2"
}

mintManyTokens() {
    minted=$(ETH_FROM=$ETH_FROM seth send $tokenOwnerModule \
        'mintManyTokens(address,address[],uint256[])' $token $1 $2 \
        --gas 6000000 --rpc-url $ETH_RPC_URL
    )
    echo "Tokens minted to $1: $2"
}

